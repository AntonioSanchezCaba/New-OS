/* =========================================================
 * Text rendering API (declared in gui/draw.h)
 * ========================================================= */

/*
 * draw_char - render one character at (x,y) in the given canvas.
 *
 * bg = 0 (fully transparent) means: only draw fg pixels, skip bg pixels.
 * This allows drawing text on top of existing content.
 */
void draw_char(canvas_t* c, int x, int y, char ch, uint32_t fg, uint32_t bg)
{
    uint8_t idx = (uint8_t)ch;
    const uint8_t* glyph = font_data[idx];
    bool transparent_bg = ((bg >> 24) == 0);

    for (int row = 0; row < FONT_H; row++) {
        int py = y + row;
        if (py < 0 || py >= c->height) continue;
        uint8_t bits = glyph[row];
        for (int col = 0; col < FONT_W; col++) {
            int px = x + col;
            if (px < 0 || px >= c->width) continue;
            if (bits & (0x80 >> col)) {
                c->pixels[py * c->stride + px] = fg;
            } else if (!transparent_bg) {
                c->pixels[py * c->stride + px] = bg;
            }
        }
    }
}

void draw_string(canvas_t* c, int x, int y, const char* str,
                 uint32_t fg, uint32_t bg)
{
    int cx = x;
    while (*str) {
        draw_char(c, cx, y, *str++, fg, bg);
        cx += FONT_W;
    }
}

void draw_printf(canvas_t* c, int x, int y, uint32_t fg, uint32_t bg,
                 const char* fmt, ...)
{
    char buf[512];
    va_list va;
    va_start(va, fmt);
    vsnprintf(buf, sizeof(buf), fmt, va);
    va_end(va);
    draw_string(c, x, y, buf, fg, bg);
}

int draw_string_width(const char* str)
{
    return (int)strlen(str) * FONT_W;
}

int draw_string_height(void)
{
    return FONT_H;
}

void draw_string_centered(canvas_t* c, int x, int y, int w, int h,
                           const char* str, uint32_t fg, uint32_t bg)
{
    int tw = draw_string_width(str);
    int cx = x + (w - tw) / 2;
    int cy = y + (h - FONT_H) / 2;
    draw_string(c, cx, cy, str, fg, bg);
}
